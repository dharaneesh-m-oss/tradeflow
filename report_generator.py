from docx import Document
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib import colors
from reportlab.lib.units import inch
from reportlab.lib.pagesizes import A4
import os
import datetime


# ==============================
# DOCX GENERATION
# ==============================
def generate_docx(data: dict, country: str, output_path: str):
    doc = Document()
    
    # Add a logo-like header or title
    title = doc.add_heading(f"{country.upper()} Customs Declaration", level=0)
    title.alignment = 1 # Center align
    
    doc.add_paragraph(f"Generated by TradeFlow AI Engine").alignment = 1
    doc.add_paragraph().add_run("_" * 50).bold = True
    
    doc.add_heading("Trade Details", level=2)
    
    # Create a table for data
    table = doc.add_table(rows=0, cols=2)
    table.style = 'Table Grid'
    
    fields = [
        ("HS Code", data.get("hs_code")),
        ("Goods Description", data.get("goods_description")),
        ("Invoice Value", f"{data.get('invoice_value', '-')} {data.get('currency', '')}"),
        ("Importer Name", data.get("importer_name")),
        ("Consignee Name", data.get("consignee_name")),
        ("Exporter Name", data.get("exporter_name")),
        ("Vessel/Flight", data.get("vessel_name")),
        ("Port of Loading", data.get("port_of_loading")),
        ("Port of Entry", data.get("arrival_port")),
        ("Net Weight", data.get("net_weight")),
        ("Gross Weight", data.get("gross_weight")),
        ("Container No", data.get("container_number")),
        ("Importer Code", data.get("importer_code")),
        ("Country of Origin", data.get("country_of_origin")),
        ("Date", data.get("date")),
    ]
    
    for label, value in fields:
        row_cells = table.add_row().cells
        row_cells[0].text = label
        row_cells[0].paragraphs[0].runs[0].bold = True
        row_cells[1].text = str(value or "-")

    doc.save(output_path)


